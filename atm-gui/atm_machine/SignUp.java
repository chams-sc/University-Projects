 // checks if PIN is an int and only 4 digits
  	boolean isValid (String pin) {
  		try {
  			// converts str to int if it fails
  			// catch will execute
  			Integer.parseInt(pin);
  			if (pin.length() == 4) {
  				return true;
  			}
  			else {
  				JOptionPane.showMessageDialog(this, "Invalid PIN. Only 4-Digit is allowed.");
  				return false;
  			}
  		}
  		catch (NumberFormatException e) {
  			JOptionPane.showMessageDialog(this, "Invalid PIN. Only numbers are allowed");
  			return false;
  		}
  	}

