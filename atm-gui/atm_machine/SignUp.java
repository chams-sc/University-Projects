//Submit Button
    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {                                         
    	String pin = String.valueOf(passfPin.getPassword());
    	
    	if(txtFName.getText().isEmpty() || txtLName.getText().isEmpty() || txtEmail.getText().isEmpty() || txtAccID.getText().isEmpty() || pin.isEmpty()) {
    		JOptionPane.showMessageDialog(this, "Missing Information");
    	}
    	else {
    		if (isValid(pin)) {
    			Connection conn= null;
        		try {
        			conn = DBConnection.dbConnect();
        			// mysql insert statement
        			String query = "INSERT INTO usertbl (FirstName, LastName, Email, AccountID, Pin, Balance) VALUES (?,?,?,?,?,?)";
        			// issues the mysql query
        			PreparedStatement Add = conn.prepareStatement(query);
        			Add.setString(1, txtFName.getText());
        			Add.setString(2, txtLName.getText());
        			Add.setString(3, txtEmail.getText());
        			Add.setString(4, txtAccID.getText());
        			Add.setInt(5, Integer.valueOf(pin));
        			Add.setInt(6, 0);
        			
        			// executes the PreparedStatement
        			Add.execute();
        			JOptionPane.showMessageDialog(this, "Succesful account creation.");
        			Clear();
        			conn.close();
        		}
        		catch(SQLException e ) {
        			// error code for duplicate entry
        			if (e.getErrorCode() == 1062) {
        				JOptionPane.showMessageDialog(this, "Account already exists. Try again.");
            			Clear();
        			}
        			else {
        				JOptionPane.showMessageDialog(this, e);
        			}
        		}
    		}
    	}
    }                   
    
